/* ══════ draw_char ══════════════════════════════════════════════════════════
 *
 * HOW BITMAP FONT RENDERING WORKS
 * ─────────────────────────────────
 * 1. Look up the glyph byte array by ASCII code:
 *      const unsigned char *bitmap = FONT_8X8[(int)c];
 *
 * 2. Loop over every row (0–7) and every column (0–7) of the 8×8 grid.
 *
 * 3. Test whether the pixel at (col, row) is "on" using the BIT7-left mask:
 *      if (bitmap[row] & (0x80 >> col)) { draw the pixel }
 *
 * 4. To draw at scale > 1, draw a (scale × scale) rectangle for each pixel.
 *
 * COMMON MISTAKE — mirror-imaged glyphs
 *   Using (0x01 << col) instead of (0x80 >> col) reverses the bit direction,
 *   making every glyph appear mirror-imaged.  Always shift FROM bit 7 (MSB)
 *   DOWN toward bit 0 to match the BIT7-left convention.
 *
 * JS equivalent: drawing to a canvas pixel by pixel using putImageData().
 */
void draw_char(SnakeBackbuffer *bb, int x, int y, char c, uint32_t color, int scale) {
    /* Ignore characters outside the ASCII table */
    if ((unsigned char)c >= 128) return;

    /* Look up glyph directly by ASCII code — no branches for digit/letter/special */
    const unsigned char *bitmap = FONT_8X8[(int)(unsigned char)c];

    int row, col;
    for (row = 0; row < GLYPH_HEIGHT; row++) {
        for (col = 0; col < GLYPH_WIDTH; col++) {
            /* Test bit: is this pixel "on"?
             * (0x80 >> col) shifts the mask right by col — tests each bit in order.
             * JS: (bitmap[row] >> (7 - col)) & 1 */
            if (bitmap[row] & (0x80 >> col)) {
                /* draw_rect handles clipping — safe to call near buffer edges */
                draw_rect(bb,
                          x + col * scale,
                          y + row * scale,
                          scale, scale,
                          color);
            }
        }
    }
}

/* ══════ draw_text ══════════════════════════════════════════════════════════
 *
 * Renders a null-terminated C string by calling draw_char() for each
 * character and advancing the horizontal cursor.
 *
 * ADVANCE — why (GLYPH_STRIDE * scale)?
 *   GLYPH_STRIDE = GLYPH_WIDTH + 1 = 9 pixels.
 *   The extra pixel adds a 1-pixel gap between characters at scale 1.
 *   At scale 2, that gap becomes 2 pixels, keeping proportions consistent.
 *
 * CALCULATING TEXT WIDTH (for centering):
 *   int text_width = strlen(text) * GLYPH_STRIDE * scale;
 *   int cx = (screen_width - text_width) / 2;
 *   draw_text(bb, cx, y, text, color, scale);
 *
 * JS equivalent: ctx.fillText(text, x, y) — the canvas handles advance
 * internally; here we manage cursor_x ourselves.
 */
void draw_text(SnakeBackbuffer *bb, int x, int y, const char *text, uint32_t color, int scale) {
    int cursor_x = x;
    for (; *text != '\0'; text++) {
        draw_char(bb, cursor_x, y, *text, color, scale);
        cursor_x += GLYPH_STRIDE * scale;  /* advance by 1 glyph + 1px gap */
    }
}
