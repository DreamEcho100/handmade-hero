/* =============================================================================
 * utils/draw-text.c — Bitmap Font + Text Rendering
 * =============================================================================
 *
 * FONT ENCODING — BIT7-LEFT
 * ─────────────────────────
 * FONT_8X8[c][row] is a uint8_t bitmask for row `row` of character `c`.
 * Bit 7 (0x80) is the LEFTMOST pixel (column 0).
 * Bit 0 (0x01) is the RIGHTMOST pixel (column 7).
 *
 * To test whether pixel at (row, col) is lit:
 *   if (FONT_8X8[c][row] & (0x80 >> col))  →  draw it
 *
 * This convention ("BIT7-left") makes the binary numbers read like the
 * pixel pattern when you look at them — bit 7 is on the left of the screen.
 *
 * Example — letter 'A' (index 0x41):
 *   Row 0:  0x18  = 0001 1000  →  ...##...
 *   Row 1:  0x3C  = 0011 1100  →  ..####..
 *   Row 2:  0x66  = 0110 0110  →  .##..##.
 *   Row 3:  0x7E  = 0111 1110  →  .######.
 *   Row 4:  0x66  = 0110 0110  →  .##..##.
 *   Row 5:  0x66  = 0110 0110  →  .##..##.
 *   Row 6:  0x66  = 0110 0110  →  .##..##.
 *   Row 7:  0x00  = 0000 0000  →  ........
 *
 * WHY INLINE THE FONT?
 * ─────────────────────
 * No file I/O at startup, no error handling for missing assets, no path
 * configuration needed.  The font data is ~1 KB and compiles directly into
 * the binary.  This is the standard approach for hobby/retro game development.
 * =============================================================================
 */

