/* ══════ draw_char ═══════════════════════════════════════════════════════════ */
void draw_char(AsteroidsBackbuffer *bb, int x, int y, char c, uint32_t color) {
    /* Cast to unsigned before range-check to handle signed char portably */
    unsigned int idx = (unsigned int)(unsigned char)c;

    /* Characters outside [0x20, 0x7E] are treated as blank space */
    if (idx >= 128) return;

    /* Iterate over each row of the 8-pixel-tall glyph */
    for (int row = 0; row < GLYPH_H; row++) {
        uint8_t bits = FONT_8X8[idx][row];

        /* Test each column bit: 0x80 >> col gives the bitmask for column col */
        for (int col = 0; col < GLYPH_W; col++) {
            if (bits & (0x80 >> col)) {
                /* Pixel is lit — draw it (clipped, no toroidal wrap for text) */
                int px = x + col;
                int py = y + row;
                if (px >= 0 && px < bb->width && py >= 0 && py < bb->height) {
                    bb->pixels[py * (bb->pitch / 4) + px] = color;
                }
            }
        }
    }
}

/* ══════ draw_text ═══════════════════════════════════════════════════════════ */
void draw_text(AsteroidsBackbuffer *bb, int x, int y,
               const char *text, uint32_t color) {
    /* Walk through the null-terminated string, advancing x by GLYPH_W per char */
    for (int i = 0; text[i] != '\0'; i++) {
        draw_char(bb, x + i * GLYPH_W, y, text[i], color);
    }
}
